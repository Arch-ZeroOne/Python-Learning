#* Checking Data Types
# Type function is used to check the type of a data in python

print(type(10)) # <class 'int'>
print(type(0.5)) # <class 'float'>
print(type(1j)) # <class 'complex'>
print(type("Windyl")) # <class 'str'>
print(type(True)) # <class 'bool'>
print(type([0,1,2,3,"Windyl"])) # <class 'list'>
print(type({'first_name':"Windyl", "last_name" : "Monton"})) #<class 'dict'>
print(type(("Mama", "Papa"))) # <class 'tuple'>
print(type({2,3,4,5})) # <class 'set'>
